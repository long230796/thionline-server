package thionline.entities.dethi;


public class Cauhoi {
	String maCau;
	String trinhDo;
	String noiDung;
	String cauA;
	String cauB;
	String cauC;
	String cauD;
	String dapAn;
	
	public Cauhoi() {}
	
	public Cauhoi(String maCau, String trinhDo, String noiDung, String cauA, String cauB, String cauC, String cauD,
			String dapAn) {
		super();
		this.maCau = maCau;
		this.trinhDo = trinhDo;
		this.noiDung = noiDung;
		this.cauA = cauA;
		this.cauB = cauB;
		this.cauC = cauC;
		this.cauD = cauD;
		this.dapAn = dapAn;
	}
	
	public String getMaCau() {
		return maCau;
	}
	public void setMaCau(String maCau) {
		this.maCau = maCau;
	}
	public String getTrinhDo() {
		return trinhDo;
	}
	public void setTrinhDo(String trinhDo) {
		this.trinhDo = trinhDo;
	}
	public String getNoiDung() {
		return noiDung;
	}
	public void setNoiDung(String noiDung) {
		this.noiDung = noiDung;
	}
	public String getCauA() {
		return cauA;
	}
	public void setCauA(String cauA) {
		this.cauA = cauA;
	}
	public String getCauB() {
		return cauB;
	}
	public void setCauB(String cauB) {
		this.cauB = cauB;
	}
	public String getCauC() {
		return cauC;
	}
	public void setCauC(String cauC) {
		this.cauC = cauC;
	}
	public String getCauD() {
		return cauD;
	}
	public void setCauD(String cauD) {
		this.cauD = cauD;
	}
	public String getDapAn() {
		return dapAn;
	}
	public void setDapAn(String dapAn) {
		this.dapAn = dapAn;
	}
	
	
	
}
